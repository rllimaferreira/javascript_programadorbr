// =     x = y     x = y
// +=    x += y    x = x + y
// -=    x -= y    x = x - y
// *=    x *= y    x = x * y
// /=    x /= y    x = x / y
// %=    x %= y    x = x % y

// Exemplo 1

var a = 30;

a += 1; // É o mesmo que a = a + 5

console.log(a);

// Exemplo 2

var a = 30;

a *= 2;

console.log(a);

// Exemplo 3

var a = 30;

a /= 2;

console.log(a);

// Exemplo 4

var a = 30;

a %= 28;

console.log(a);

