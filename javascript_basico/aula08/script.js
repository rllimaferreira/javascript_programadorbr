// Operadores Comparativos

// == igual a
// === valor igual e igual
// ! = não é igual
// !== não igual ou não igual
// > maior que
// < menor que
// >= maior que ou igual a
// <= menor ou igual a

// Exemplo 1

console.log(2 == 3);

// Exemplo 2

var a = 3;
