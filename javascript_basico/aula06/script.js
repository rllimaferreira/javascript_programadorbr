// Operadores Aritméticos

// + Adição
// - Subtração
// * Multiplicação
// / Divisão
// % Módulo
// ++ Incremento
// -- Decremento

// Exemplo 1

var a = 10;
var b = 15;

var c = a / b;

console.log(c);

// Exemplo 2

var a = 10;
var b = 15;

var c = a % b;

console.log(c);

// Exemplo 3

var a = 30;

a++; // É o mesmo que a = a + 1

console.log(a); // Imprime 30

// Exemplo 4

var a = 30;

var b = a--;

console.log(b); // Imprime 30
console.log(a); // Imprime 29 - Houve a decrementação do a, mas só depois

// Exemplo 5

var a = 30;

var b = --a;

console.log(b); // Imprime 29

// Importante - A ordem importa bastante na programação